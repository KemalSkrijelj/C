#include <stdio.h>

int main()
{
  int firstNumber = 11;
  int secondNumber = 120;

  float thirdNumber = 121221.33452;

  char someChar = 'a';

  printf("First value: %d \n", firstNumber);
  printf("Second value: %d \n", secondNumber);
  printf("Some Char value: %c \n", someChar);
  printf("Third Num value: %f \n", thirdNumber);

  return 0;
}
